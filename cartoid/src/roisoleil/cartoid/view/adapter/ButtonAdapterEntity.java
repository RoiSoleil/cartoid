package roisoleil.cartoid.view.adapter;

public class ButtonAdapterEntity {
	private int code;
	private String text;

	public ButtonAdapterEntity() {
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
}
